#ifndef VEC_ASSI_H
#define VEC_ASSI_H

#define vec_ASSIGN2(va, vb) \
{ \
  (va)[0] = (vb)[0]; \
  (va)[1] = (vb)[1]; \
}
#define vec_ASSIGN3(va, vb) \
{ \
  (va)[0] = (vb)[0]; \
  (va)[1] = (vb)[1]; \
  (va)[2] = (vb)[2]; \
}
#define vec_ASSIGN4(va, vb) \
{ \
  (va)[0] = (vb)[0]; \
  (va)[1] = (vb)[1]; \
  (va)[2] = (vb)[2]; \
  (va)[3] = (vb)[3]; \
}

#endif
